<template>
  <SponsorInvoiceLayout
    title="Monthly Report"
    :subtitle="`Finance monthly rollup for ${selectedMonthLabel}`"
    :has-error="!!error"
  >
    <template #meta>
      <strong>Report Month:</strong><span>{{ selectedMonthLabel }}</span>
      <strong>Branch:</strong><span>{{ formatPrintableName(clinicName, "All branches") }}</span>
      <strong>Generated:</strong><span>{{ generatedAtLabel }}</span>
      <strong>Generated By:</strong><span>{{ formatPrintableName(generatedByLabel, "--") }}</span>
    </template>

    <template #toolbar>
      <Button label="Print" icon="pi pi-print" @click="printPage('landscape')" />
      <Button label="Close" icon="pi pi-times" severity="secondary" outlined @click="goBack" />
    </template>

    <template v-if="error" #error>
      {{ error }}
    </template>

    <template v-if="!error">
      <section class="monthly-report-section">
        <h2 class="monthly-report-section-title">Finance Summary</h2>
        <div class="monthly-report-metrics">
          <div v-for="metric in financeMetrics" :key="metric.label" class="monthly-report-metric">
            <div class="monthly-report-metric__label">{{ metric.label }}</div>
            <div class="monthly-report-metric__value">{{ metric.value }}</div>
          </div>
        </div>
      </section>

      <section class="monthly-report-section">
        <h2 class="monthly-report-section-title">Daily Rollup</h2>
        <div class="table-wrap">
          <table class="summary-table monthly-report-table">
            <thead>
              <tr>
                <th class="text-center report-number-column">No.</th>
                <th class="text-left">Date</th>
                <th class="text-right">Income Rows</th>
                <th class="text-right">Expense Rows</th>
                <th class="text-right">Gross</th>
                <th class="text-right">Outstanding</th>
                <th class="text-right">Expenses</th>
                <th class="text-right">Net Cash</th>
              </tr>
            </thead>
            <tbody>
              <tr v-if="!days.length">
                <td colspan="8" class="empty-row">No finance activity found for this month.</td>
              </tr>
              <tr v-for="(row, index) in days" :key="row.report_date">
                <td class="text-center report-number-column">{{ index + 1 }}</td>
                <td>{{ formatMonthDay(row.report_date) }}</td>
                <td class="text-right">{{ row.income_entry_count }}</td>
                <td class="text-right">{{ row.expense_entry_count }}</td>
                <td class="text-right">{{ asCurrency(row.gross_income) }}</td>
                <td class="text-right">{{ asCurrency(row.outstanding_balance) }}</td>
                <td class="text-right">{{ asCurrency(row.expense_total) }}</td>
                <td class="text-right">{{ asCurrency(row.net_cash) }}</td>
              </tr>
            </tbody>
          </table>
        </div>
      </section>

      <section class="monthly-report-section">
        <h2 class="monthly-report-section-title">Monthly Totals</h2>
        <div class="monthly-report-metrics monthly-report-metrics--totals">
          <div class="monthly-report-metric">
            <div class="monthly-report-metric__label">Gross Charge</div>
