<script setup lang="ts">
import { computed, onMounted, ref } from "vue"
import { useRoute } from "vue-router"
import Button from "primevue/button"
import {
  billingPhase1Service,
  type MonthlyIncomeExpenseDay,
  type MonthlyIncomeExpenseReport
} from "@/features/billing/api/billing-phase1.service"
import SponsorInvoiceLayout from "@/features/shared/invoices/SponsorInvoiceLayout.vue"
import { useSponsorInvoicePrintActions } from "@/features/shared/invoices/sponsor-invoice.shared"
import { useAuthSessionStore } from "@/stores/auth-session.store"

type Metric = {
  label: string
  value: string
}

const route = useRoute()
const authSession = useAuthSessionStore()
const { printPage, goBack } = useSponsorInvoicePrintActions()

const report = ref<MonthlyIncomeExpenseReport>()
const error = ref("")
const generatedAt = ref(new Date())

const selectedMonth = computed(() => parseMonthQuery(route.query.month) || toMonthParam(new Date()))
const selectedDate = computed(() => parseDateQuery(route.query.date) || `${selectedMonth.value}-01`)
const clinicId = computed(() => parsePositiveNumber(route.query.clinic_id))
const queryClinicName = computed(() => String(route.query.clinic_name ?? "").trim())

const selectedMonthLabel = computed(() => {
  const [year, month] = selectedMonth.value.split("-").map(Number)
  return new Date(year, month - 1, 1).toLocaleDateString("en-PH", {
    year: "numeric",
    month: "long"
  })
})

const generatedAtLabel = computed(() => formatDateTime(generatedAt.value.toISOString()))
const generatedByLabel = computed(() => authSession.staffName || "--")
const clinicName = computed(() => queryClinicName.value || "All branches")

const emptySummary = {
  active_day_count: 0,
  days_with_income: 0,
  days_with_expenses: 0,
  income_entry_count: 0,
  expense_entry_count: 0,
  unbilled_appointment_count: 0,
  incomplete_billing_count: 0,
  partial_billing_count: 0,
  gross_income: 0,
  cash_collected: 0,
  tendered_cash: 0,
  tendered_ewallet: 0,
  tendered_debit_credit: 0,
  tendered_other: 0,
  outstanding_balance: 0,
  incomplete_billing_balance: 0,
  expense_total: 0,
  net_cash: 0
}

const summary = computed(() => report.value?.summary ?? emptySummary)
const days = computed<MonthlyIncomeExpenseDay[]>(() => report.value?.days ?? [])

const financeMetrics = computed<Metric[]>(() => [
  { label: "Gross Charges", value: asCurrency(summary.value.gross_income) },
  { label: "Cash", value: asCurrency(summary.value.tendered_cash) },
  { label: "E-wallets", value: asCurrency(summary.value.tendered_ewallet) },
  { label: "Debit/Credit", value: asCurrency(summary.value.tendered_debit_credit) },
  { label: "Outstanding", value: asCurrency(summary.value.outstanding_balance) },
  { label: "Incomplete Billings", value: `${summary.value.incomplete_billing_count} / ${asCurrency(summary.value.incomplete_billing_balance)}` },
  { label: "Expenses", value: asCurrency(summary.value.expense_total) },
  { label: "Net Cash", value: asCurrency(summary.value.net_cash) },
  { label: "Active Days", value: String(summary.value.active_day_count) }
])

const formatPrintableName = (value?: string | null, fallback = "N/A"): string => {
  const name = String(value ?? "").trim()
  return name ? name.toUpperCase() : fallback
}

const asCurrency = (value: number): string =>
  new Intl.NumberFormat("en-PH", {
    style: "currency",
    currency: "PHP",
    maximumFractionDigits: 2
  }).format(Number(value ?? 0))

const toMonthParam = (value: Date): string => {
  const year = value.getFullYear()
  const month = String(value.getMonth() + 1).padStart(2, "0")
  return `${year}-${month}`
}

const parseMonthQuery = (value: unknown): string | null => {
  const raw = String(value ?? "").trim()
  return /^\d{4}-\d{2}$/.test(raw) ? raw : null
}

const parseDateQuery = (value: unknown): string | null => {
  const raw = String(value ?? "").trim()
  return /^\d{4}-\d{2}-\d{2}$/.test(raw) ? raw : null
}

const parsePositiveNumber = (value: unknown): number | undefined => {
  const numeric = Number(value)
  return Number.isFinite(numeric) && numeric > 0 ? numeric : undefined
}

const formatMonthDay = (value: string): string =>
  new Date(`${value}T00:00:00`).toLocaleDateString("en-PH", {
    month: "short",
    day: "numeric",
    weekday: "short"
  })

const formatDateTime = (value?: string | null): string => {
  if (!value) return "--"
  const date = new Date(value)
  if (Number.isNaN(date.getTime())) return "--"
  return date.toLocaleString("en-PH", {
    month: "short",
    day: "numeric",
    year: "numeric",
    hour: "numeric",
    minute: "2-digit"
  })
}

onMounted(async () => {
  try {
    await authSession.ensureLoaded()
  } catch {
    error.value = "Monthly report could not verify the signed-in user."
    return
  }

  if (!authSession.isOwnerEquivalent) {
    error.value = "Monthly report is available only to owner-level users."
    return
  }

  try {
    report.value = await billingPhase1Service.getMonthlyIncomeExpense(
      selectedMonth.value,
      selectedDate.value,
      clinicId.value
    )
  } catch (caught) {
    error.value = caught instanceof Error ? caught.message : "Monthly report could not be loaded."
  }
})
</script>

